// Unsubscribed from emails
exports.unsubscribe = catchAsync(async (req, res, next) => {
  const { email } = req.params;
  let unsubscribedUser = await User.findOne({ email });
  unsubscribedUser.subscribed = false;
  await unsubscribedUser.save();
  // Use path.join to construct the path to the Pug file
  const pugFilePath = path.join(__dirname, '..', 'views', 'unsubscribe.pug');

  // Render the Pug template
  const html = pug.renderFile(pugFilePath);

  // Send the HTML as a response
  res.send(html);
});
